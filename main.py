# Create a Dictionary
cats = {"Ginger" : "Susie","Black" : "Bob", "White" : "Joe"}
print(cats)

# Accessing values from a dictionary
my_cat = cats["Ginger"]
print(f"My favourite cat is {my_cat}!")

# Get the list of Keys
list_keys = list(cats.keys())
print(list_keys)

# Get the list of Values
list_values = list(cats.values())
print(list_values)